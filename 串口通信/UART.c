#include <REGX52.H>

//*************************************************

//串口初始化

//*************************************************
void Uart_Init()
{
	SCON = 0x40;
	PCON |= 0x80;
	TMOD &= 0x0f;
	TMOD |= 0x20;
	TH1 = 0xf4;
	TL1 = 0xf4;
	TR1 = 1;
	ET1 = 0;

	
}


//*************************************************

//串口发送 

//*************************************************
void Uart_Send(unsigned char send)
{
	SBUF = send;
	while(0 == TI);
	TI = 0;
}

