#include <REGX52.H>

//*****************************************

//定时器T0初始化
//默认0.01s

//*****************************************
void T0_Initial()
{
	//高四位不变
	TMOD = TMOD & 0xf0;
	TMOD = TMOD | 0x01;
	TF0 = 0;
	TR0 = 1;
	
	TH0 = (65536-10000)/256;
	TL0 = (65536-10000)%256;
	
	//中断
	ET0 = 1;
	EA = 1;
	PT0 = 0;
	

}
