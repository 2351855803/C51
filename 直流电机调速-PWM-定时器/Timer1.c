#include <REGX52.H>


//*************************************************

//功能：定时器0初始化
//返回类型：无
//形参：无

//*************************************************

void Timer0_Init()
{
	TMOD &= 0xf0;
	TMOD |= 0x01;
	TH0 = (65535-100);
	TL0 = (65535-100);
	TF0 = 0;
	TR0 = 1;
	ET0 = 1;
	EA = 1;
	
}